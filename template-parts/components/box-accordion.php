  /* Track which card is open per row */
  var pdaccActive = { 0: null, 1: null };

  /* Build the label + paragraph HTML for a card's sections */
  function pdaccBuildHTML(sections) {
    return sections.map(function (s) {
      var html = '';
      if (s.label) {
        html += '<span class="pdacc-section-label">' + s.label + '</span>';
      }
      html += '<p>' + s.text + '</p>';
      return html;
    }).join('');
  }

  function pdaccToggle(id) {
    var item = pdaccData[id];
    var row = item.row;
    var spacer = document.getElementById('pdacc-spacer-' + row);
    var trigger = document.getElementById('pdacc-trigger-' + id);
    var icon = document.getElementById('pdacc-icon-' + id);
    var card = document.querySelector('[data-id="' + id + '"]');
    var prev = pdaccActive[row];

    /* Close previously open card in this row */
    if (prev && prev !== id) {
      var prevTrigger = document.getElementById('pdacc-trigger-' + prev);
      var prevIcon = document.getElementById('pdacc-icon-' + prev);
      var prevPanel = document.getElementById('pdacc-panel-' + prev);
      var prevCard = document.querySelector('[data-id="' + prev + '"]');

      prevTrigger.setAttribute('aria-expanded', 'false');
      prevCard.classList.remove('pdacc-is-open');
      prevIcon.textContent = '+';
      prevPanel.hidden = true;
    }

    /* Toggle the clicked card */
    var isOpen = (prev === id);
    var thisPanel = document.getElementById('pdacc-panel-' + id);

    if (isOpen) {
      /* Close */
      trigger.setAttribute('aria-expanded', 'false');
      card.classList.remove('pdacc-is-open');
      icon.textContent = '+';
      thisPanel.hidden = true;
      spacer.hidden = true;
      pdaccActive[row] = null;
    } else {
      /* Open — populate content first */
      var leftEl = document.getElementById('pdacc-left-' + id);
      var imgEl = document.getElementById('pdacc-img-' + id);
      leftEl.innerHTML = pdaccBuildHTML(item.sections);
      imgEl.src = item.img;

      thisPanel.hidden = false;
      spacer.hidden = false;
      trigger.setAttribute('aria-expanded', 'true');
      card.classList.add('pdacc-is-open');
      icon.textContent = '−';
      pdaccActive[row] = id;

      /* Move focus to the panel so screen readers read it,
         and scroll it into view smoothly */
      var liveRegion = document.getElementById('pdacc-panel-focus-' + row);

      if (liveRegion) {
        liveRegion.focus();
        liveRegion.scrollIntoView({
          behavior: 'smooth',
          block: 'nearest'
        });
      }
    }
  }
